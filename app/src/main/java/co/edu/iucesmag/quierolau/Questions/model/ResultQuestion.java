package co.edu.iucesmag.quierolau.Questions.model;

public class ResultQuestion {
    private int nropreg;
    private String respreg;
    private Boolean correcto;

    public int getNropreg() {
        return nropreg;
    }

    public void setNropreg(int nropreg) {
        this.nropreg = nropreg;
    }

    public String getRespreg() {
        return respreg;
    }

    public void setRespreg(String respreg) {
        this.respreg = respreg;
    }

    public Boolean getCorrecto() {
        return correcto;
    }

    public void setCorrecto(Boolean correcto) {
        this.correcto = correcto;
    }
}
